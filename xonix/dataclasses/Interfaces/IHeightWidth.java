package xonix.dataclasses.Interfaces;


/**
 * Interface representing something with a height and width
 * */
public interface IHeightWidth {

    /**
     * Returns height
     * @return height
     * */
    int getHeight();

    /**
     * Sets the height
     * @param height value
     * */
    void setHeight(int height);

    /**
     * Returns width
     * @return width
     * */
    int getWidth();

    /**
     * Sets the width
     * @param width value
     * */
    void setWidth(int width);

}